#!/usr/bin/env node
// Mnemosyne — C4.1 live smoke for /api/generate-contract (Aegis-required battery, thread 0017).
// Creates a throwaway ACTIVE member + non-member, generates an MOU and an SOW from sample briefs via real
// member JWTs, and asserts: constants present verbatim, fills substituted (no invented figures), draft slots
// on-brief, NO vendor/product brand names, NO AI-disclosure phrasing, NO leftover {{ }}/<<<SLOT>>> markers,
// same-type sources only, exemplar text not returned, and all fail-closed paths (401/403/400). Bakes in the
// prohibited-content scanner Aegis requires (manual gate for C4.1; previews the C4.2 automated scanner).
// No persistence in C4.1, so cleanup only removes the throwaway users.
//
// Run: node --env-file=.env.local scripts/smoke-generate-contract.mjs

import { createClient } from '@supabase/supabase-js'

const BASE = process.env.SMOKE_BASE || 'https://project-mnemosyne.pages.dev'
const URL = process.env.VITE_SUPABASE_URL
const SERVICE = process.env.SUPABASE_SERVICE_ROLE_KEY
const PUB = process.env.SUPABASE_PUBLISHABLE_KEY || process.env.VITE_SUPABASE_ANON_KEY
if (!URL || !SERVICE || !PUB) { console.error('missing env (URL / SERVICE / PUBLISHABLE)'); process.exit(1) }

const admin = createClient(URL, SERVICE, { auth: { persistSession: false, autoRefreshToken: false } })
const PW = 'Smoke!' + Math.abs(URL.length * 7919).toString(36) + 'xZ9'
const stamp = process.env.SMOKE_STAMP || String(Date.now())
const memberEmail = `smoke-gen-member-${stamp}@mnemosyne.test`
const nonmemberEmail = `smoke-gen-nonmember-${stamp}@mnemosyne.test`

let pass = 0, fail = 0
function check(name, ok, extra = '') { ok ? pass++ : fail++; console.log(`${ok ? 'PASS' : 'FAIL'}  ${name}${extra ? '  — ' + extra : ''}`) }

async function gen(body, token, raw = false) {
  const headers = { 'content-type': 'application/json' }
  if (token) headers.authorization = `Bearer ${token}`
  const res = await fetch(`${BASE}/api/generate-contract`, { method: 'POST', headers, body: raw ? body : JSON.stringify(body) })
  let data = null; try { data = await res.json() } catch {}
  return { status: res.status, data, rawText: JSON.stringify(data ?? '') }
}
async function signIn(email) {
  const c = createClient(URL, PUB, { auth: { persistSession: false, autoRefreshToken: false } })
  const { data, error } = await c.auth.signInWithPassword({ email, password: PW })
  if (error || !data?.session?.access_token) throw new Error(`sign-in failed for ${email}: ${error?.message}`)
  return data.session.access_token
}

// prohibited-content scanners
const BRANDS = /\b(supabase|cloudflare|stripe|sendgrid|resend|mailchimp|gemini|openai|chatgpt|claude|anthropic|vercel|netlify|heroku|\baws\b|amazon web services|azure|google cloud|\bgcp\b|github|gitlab|postgres|postgresql|\breact\b|\bvite\b|tailwind|twilio)\b/i
const AI_DISCLOSURE = /\b(artificial intelligence|ai-generated|ai-assisted|generated by ai|using ai|large language model|\bllm\b|machine learning|\bA\.I\.\b)\b/i
const SECRETY = /service_role|sb_secret_|AIza[0-9A-Za-z_-]{20}|x-goog-api-key|extracted_text|eyJ[A-Za-z0-9_-]{20,}\./
const MARKERS = /\{\{|<<<SLOT|<<<ENDSLOT/

