const MOU_CONSTANTS = ['MEMORANDUM OF UNDERSTANDING', '4ward Motion Solutions, Inc.', 'Co-Founder and CTO', 'State of Delaware', 'Pre-Existing IP', 'Confidential Information', 'TOTAL CUMULATIVE LIABILITY', 'AS IS', 'independent contractor']
const SOW_CONSTANTS = ['Statement of Work', '4ward Motion Solutions, Inc.', 'Co-Founder and CTO', 'Memorandum of Understanding', 'Change Order', 'good faith']

let memberUid, nonmemberUid
async function setup() {
  const m = await admin.auth.admin.createUser({ email: memberEmail, password: PW, email_confirm: true })
  if (m.error) throw new Error('createUser member: ' + m.error.message)
  memberUid = m.data.user.id
  const ins = await admin.from('team_members').insert({ id: memberUid, full_name: 'Smoke Gen Member', email: memberEmail, role: 'member', active: true })
  if (ins.error) throw new Error('insert team_members: ' + ins.error.message)
  const n = await admin.auth.admin.createUser({ email: nonmemberEmail, password: PW, email_confirm: true })
  if (n.error) throw new Error('createUser nonmember: ' + n.error.message)
  nonmemberUid = n.data.user.id
}
async function cleanup() {
  if (memberUid) await admin.auth.admin.deleteUser(memberUid)
  if (nonmemberUid) await admin.auth.admin.deleteUser(nonmemberUid)
}

function assertDoc(label, r, fills, constants, expectedType) {
  check(`${label}: 200 + markdown`, r.status === 200 && typeof r.data?.markdown === 'string' && r.data.markdown.length > 1500, `status=${r.status} len=${r.data?.markdown?.length}`)
  const md = r.data?.markdown ?? ''
  const missingConst = constants.filter((c) => !md.includes(c))
  check(`${label}: constants present verbatim`, missingConst.length === 0, missingConst.length ? `missing: ${missingConst.join(' | ')}` : '')
  const missingFill = Object.entries(fills).filter(([k, v]) => ['project_name', 'client_entity', 'engagement_ref'].includes(k) && !md.includes(v)).map(([k]) => k)
  check(`${label}: key fills substituted`, missingFill.length === 0, missingFill.length ? `missing: ${missingFill.join(', ')}` : '')
  check(`${label}: no leftover markers`, !MARKERS.test(md))
  const brand = md.match(BRANDS)
  check(`${label}: no vendor/product brand names`, !brand, brand ? `found "${brand[0]}"` : '')
  const ai = md.match(AI_DISCLOSURE)
  check(`${label}: no AI-disclosure phrasing`, !ai, ai ? `found "${ai[0]}"` : '')
  check(`${label}: no secret/raw-text markers in response`, !SECRETY.test(r.rawText))
  const srcs = r.data?.sources ?? []
  check(`${label}: sources are same-type only (${expectedType})`, srcs.every((s) => s.doc_type === expectedType), `types=${[...new Set(srcs.map((s) => s.doc_type))].join(',') || 'none'}`)
  if ((r.data?.warnings ?? []).length) console.log(`      note: ${label} warnings → ${r.data.warnings.join(' | ')} (review-only, not a failure)`)
}

async function run() {
  await setup()
  const memberJwt = await signIn(memberEmail)
  const nonmemberJwt = await signIn(nonmemberEmail)

  console.log('Generating MOU…')
  assertDoc('MOU', await gen({ doc_type: 'mou', fields: MOU_FIELDS, ground: true }, memberJwt), MOU_FIELDS, MOU_CONSTANTS, 'mou')
  console.log('Generating SOW…')
  assertDoc('SOW', await gen({ doc_type: 'sow', fields: SOW_FIELDS, ground: true }, memberJwt), SOW_FIELDS, SOW_CONSTANTS, 'sow')

  // fail-closed paths
  check('missing JWT -> 401', (await gen({ doc_type: 'mou', fields: MOU_FIELDS }, null)).status === 401)
  check('invalid JWT -> 401', (await gen({ doc_type: 'mou', fields: MOU_FIELDS }, 'not-a-jwt')).status === 401)
  check('non-member -> 403', (await gen({ doc_type: 'mou', fields: MOU_FIELDS }, nonmemberJwt)).status === 403)
  check('bad doc_type -> 400', (await gen({ doc_type: 'proposal', fields: {} }, memberJwt)).status === 400)
  check('unknown slot -> 400', (await gen({ doc_type: 'mou', fields: { bogus_slot: 'x' } }, memberJwt)).status === 400)
  check('missing required -> 400', (await gen({ doc_type: 'mou', fields: { client_entity: 'X' } }, memberJwt)).status === 400)
  check('non-string field -> 400', (await gen({ doc_type: 'mou', fields: { project_name: 123 } }, memberJwt)).status === 400)
  check('oversize field -> 400', (await gen({ doc_type: 'mou', fields: { project_name: 'a'.repeat(201) } }, memberJwt)).status === 400)
  check('extra top-level key -> 400', (await gen({ doc_type: 'mou', fields: MOU_FIELDS, bogus: 1 }, memberJwt)).status === 400)
}

run().catch((e) => { console.error('SMOKE ERROR:', e.message); fail++ })
  .finally(async () => {
    try { await cleanup() } catch (e) { console.error('cleanup warning:', e.message) }
    console.log(`\n${fail === 0 ? '✅ ALL PASS' : '❌ FAILURES'} — ${pass} pass / ${fail} fail`)
    process.exit(fail === 0 ? 0 : 1)
  })
